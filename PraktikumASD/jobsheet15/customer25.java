public class customer25 {
    public int id;
    public String name;

    public customer25() {
    }

    public customer25(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return " ID: " + this.id + " Nama: " + this.name;
    }
}